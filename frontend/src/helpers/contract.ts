// endereco do contrato BasicMixer deployado.
//
// 🔑 CONCEITO: O que e uma ABI?
// A ABI e como um "manual de instrucoes" do contrato. Ela descreve:
//   - Quais funcoes existem e seus parametros
//   - Quais eventos o contrato emite
//   - Quais erros customizados podem ocorrer
//
// O frontend usa a ABI para saber COMO se comunicar com o contrato.
// Sem ela, nao saberiamos como formatar as chamadas.
//
// ⚙️ COMO FUNCIONA:
// wagmi/viem usam a ABI + endereco para:
//   - Codificar chamadas de funcao (ex: deposit(commitment))
//   - Decodificar respostas (ex: currentRoot() retorna bytes32)
//   - Parsear eventos emitidos (ex: Deposit(commitment, leafIndex))
//   - Detectar erros customizados (ex: NullifierAlreadyUsed)
//
// ⚠️ IMPORTANTE: Se voce deployou um novo contrato no workshop,
// atualize BASIC_MIXER_ADDRESS com o novo endereco!
// ============================================================

